"""Entity-centric clustering for high cohesion and low coupling."""

from typing import List, Dict, Any, Set, Tuple, Optional
import re
from collections import defaultdict
from sklearn.metrics.pairwise import cosine_similarity
from sklearn.cluster import AgglomerativeClustering
import numpy as np


class EntityClusterer:
    """
    Cluster APIs based on entity-centric approach for high cohesion and low coupling.

    Core Strategy:
    1. Extract primary entity (business anchor) from API paths
    2. Analyze response schema to determine entity ownership
    3. Use constrained hierarchical clustering with strict thresholds
    4. Ensure atomic capabilities with CRUD completeness
    """

    # Common noise words to ignore in path analysis
    NOISE_WORDS = {
        'api', 'v1', 'v2', 'v3', 'rest', 'service', 'json', 'xml',
        'do', 'action', 'controller', 'endpoint', 'resource'
    }

    # HTTP method to CRUD mapping
    METHOD_TO_CRUD = {
        'POST': 'create',
        'GET': 'read',
        'PUT': 'update',
        'PATCH': 'update',
        'DELETE': 'delete'
    }

    def __init__(self,
                 similarity_threshold: float = 0.85,
                 response_weight: float = 0.7,
                 request_weight: float = 0.3):
        """
        Initialize entity-centric clusterer.

        Args:
            similarity_threshold: Strict threshold for merging clusters (0.85 recommended)
            response_weight: Weight for response schema in similarity (0.7 recommended)
            request_weight: Weight for request schema in similarity (0.3 recommended)
        """
        self.similarity_threshold = similarity_threshold
        self.response_weight = response_weight
        self.request_weight = request_weight

    def cluster(self, apis: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        """
        Cluster APIs using entity-centric approach.

        Returns:
            List of APIs with cluster assignments
        """
        if not apis:
            return []

        # Step 1: Extract primary entities for each API
        for api in apis:
            api['primary_entity'] = self._extract_primary_entity(api)
            api['entity_anchor'] = self._extract_path_anchor(api)

        # Step 2: Build feature vectors
        feature_vectors = self._build_feature_vectors(apis)

        # Step 3: Constrained hierarchical clustering
        clustered_apis = self._hierarchical_cluster(apis, feature_vectors)

        # Step 4: Refine clusters for CRUD completeness
        clustered_apis = self._refine_crud_clusters(clustered_apis)

        return clustered_apis

    def _extract_path_anchor(self, api: Dict[str, Any]) -> str:
        """
        Extract the business anchor (primary entity) from API path.

        Logic: Strip noise words, find the entity noun (usually second-to-last segment).
        Example: /admin-api/erp/purchase-order/create -> 'purchase-order'
        Example: /api/v1/user/order/list -> 'order'
        """
        path = api.get('path', '')
        normalized_path = api.get('normalized_path', path)

        # Remove common prefixes and suffixes
        path_clean = re.sub(r'\.(do|json|xml)$', '', normalized_path)

        # Split into segments
        segments = [s for s in path_clean.split('/') if s]

        # Filter out noise words and parameters
        meaningful_segments = []
        action_words = {'list', 'page', 'export-excel', 'get', 'create', 'update', 'delete',
                       'add', 'remove', 'query', 'search', 'find', 'detail', 'info',
                       'time-summary', 'summary', 'dashboard-time-summary', 'bill-time-summary',
                       'order-time-summary', 'update-status'}

        for seg in segments:
            # Skip parameters
            if seg.startswith('{') or seg.startswith(':'):
                continue
            # Skip noise words
            seg_lower = seg.lower()
            if seg_lower in self.NOISE_WORDS:
                continue
            # Skip pure numbers
            if seg.isdigit():
                continue
            meaningful_segments.append((seg_lower, seg_lower in action_words))

        # Find the entity: the last non-action segment
        entity_segments = [seg for seg, is_action in meaningful_segments if not is_action]

        if entity_segments:
            # Return the last entity segment (usually the primary entity)
            anchor = entity_segments[-1]
            anchor = anchor.strip('-_')
            if anchor:
                return anchor

        # Fallback to first tag or 'unknown'
        tags = api.get('tags', [])
        if tags and tags[0] != 'AutoGenerated':
            return tags[0].lower()

        return 'unknown'

    def _extract_primary_entity(self, api: Dict[str, Any]) -> str:
        """
        Determine the primary entity based on response schema.

        Response schema determines what the API "delivers", which is the true entity.
        """
        # First check response schema (parsed by swagger_parser)
        response_schemas = api.get('response_schemas', {})

        # Look for successful response (200, 201, etc.)
        for status_code in ['200', '201', '202']:
            if status_code in response_schemas:
                schema = response_schemas[status_code]

                # Check if schema has a clear entity name
                entity_name = self._extract_entity_from_schema(schema)
                if entity_name:
                    return entity_name

        # Fallback to path anchor
        return self._extract_path_anchor(api)

    def _extract_entity_from_schema(self, schema: Dict[str, Any]) -> Optional[str]:
        """
        Extract entity name from schema definition.

        Looks for $ref, title, or object structure hints.
        """
        if not schema:
            return None

        # Check for $ref (e.g., "#/definitions/User")
        ref = schema.get('$ref', '')
        if ref:
            # Extract last part of reference
            parts = ref.split('/')
            if parts:
                entity = parts[-1].lower()
                # Remove common suffixes
                entity = re.sub(r'(dto|vo|entity|model|response|request)$', '', entity)
                return entity.strip()

        # Check for title
        title = schema.get('title', '')
        if title:
            entity = title.lower()
            entity = re.sub(r'(dto|vo|entity|model|response|request)$', '', entity)
            return entity.strip()

        # Check for array items
        if schema.get('type') == 'array':
            items = schema.get('items', {})
            return self._extract_entity_from_schema(items)

        return None

    def _build_feature_vectors(self, apis: List[Dict[str, Any]]) -> np.ndarray:
        """
        Build feature vectors for clustering.

        Vector = [Path_Anchor] × 0.5 + [Response_Schema] × 0.4 + [HTTP_Method] × 0.1
        """
        n_apis = len(apis)

        # Extract unique entities and methods
        all_entities = list(set(api['entity_anchor'] for api in apis))
        all_methods = list(set(api['method'] for api in apis))

        # Collect all schema fields
        all_schema_fields = set()
        for api in apis:
            schema_fingerprint = self._get_schema_fingerprint(api, 'response')
            all_schema_fields.update(schema_fingerprint)
        all_schema_fields = sorted(all_schema_fields)

        entity_to_idx = {e: i for i, e in enumerate(all_entities)}
        method_to_idx = {m: i for i, m in enumerate(all_methods)}
        field_to_idx = {f: i for i, f in enumerate(all_schema_fields)}

        # Build feature matrix
        n_features = len(all_entities) + len(all_schema_fields) + len(all_methods)
        features = np.zeros((n_apis, n_features))

        for i, api in enumerate(apis):
            # Path anchor feature (50% weight)
            entity_idx = entity_to_idx[api['entity_anchor']]
            features[i, entity_idx] = 0.5

            # Response schema features (40% weight distributed across fields)
            schema_fingerprint = self._get_schema_fingerprint(api, 'response')
            if schema_fingerprint:
                weight_per_field = 0.4 / len(schema_fingerprint) if schema_fingerprint else 0
                for field in schema_fingerprint:
                    field_idx = field_to_idx[field]
                    features[i, len(all_entities) + field_idx] = weight_per_field

            # HTTP method feature (10% weight)
            method_idx = method_to_idx[api['method']]
            features[i, len(all_entities) + len(all_schema_fields) + method_idx] = 0.1

        return features

    def _get_schema_fingerprint(self, api: Dict[str, Any], schema_type: str = 'response') -> Set[str]:
        """
        Get schema fingerprint (set of field names).

        Args:
            schema_type: 'response' or 'request'
        """
        fields = set()

        if schema_type == 'response':
            response_schemas = api.get('response_schemas', {})
            for status_code in ['200', '201', '202']:
                if status_code in response_schemas:
                    schema = response_schemas[status_code]
                    fields.update(self._extract_schema_fields(schema))
        else:
            # Request parameters
            request_schema = api.get('request_schema')
            if request_schema:
                fields.update(self._extract_schema_fields(request_schema))

            # Also check parameters
            parameters = api.get('parameters', [])
            for param in parameters:
                if isinstance(param, dict):
                    fields.add(param.get('name', ''))

        return fields

    def _extract_schema_fields(self, schema: Dict[str, Any], depth: int = 0) -> Set[str]:
        """
        Recursively extract field names from schema.
        """
        if depth > 3:  # Prevent infinite recursion
            return set()

        fields = set()

        if not schema:
            return fields

        # Handle object properties
        properties = schema.get('properties', {})
        for field_name in properties.keys():
            fields.add(field_name.lower())

        # Handle array items
        if schema.get('type') == 'array':
            items = schema.get('items', {})
            fields.update(self._extract_schema_fields(items, depth + 1))

        # Handle allOf, anyOf, oneOf
        for key in ['allOf', 'anyOf', 'oneOf']:
            if key in schema:
                for sub_schema in schema[key]:
                    fields.update(self._extract_schema_fields(sub_schema, depth + 1))

        return fields

    def _hierarchical_cluster(self, apis: List[Dict[str, Any]],
                             features: np.ndarray) -> List[Dict[str, Any]]:
        """
        Perform constrained hierarchical clustering with strict threshold.

        Uses Agglomerative Clustering with distance threshold to ensure
        high purity clusters.
        """
        n_apis = len(apis)

        if n_apis == 1:
            apis[0]['cluster'] = 0
            apis[0]['cluster_type'] = 'atomic'
            return apis

        # Calculate similarity matrix
        similarity_matrix = cosine_similarity(features)

        # Convert to distance matrix
        distance_matrix = 1 - similarity_matrix

        # Perform hierarchical clustering with distance threshold
        clustering = AgglomerativeClustering(
            n_clusters=None,
            distance_threshold=1 - self.similarity_threshold,
            metric='precomputed',
            linkage='average'
        )

        labels = clustering.fit_predict(distance_matrix)

        # Assign cluster labels
        for i, api in enumerate(apis):
            api['cluster'] = int(labels[i])
            api['cluster_type'] = 'semantic'

        # Mark single-API clusters as atomic
        cluster_sizes = defaultdict(int)
        for label in labels:
            cluster_sizes[label] += 1

        for api in apis:
            if cluster_sizes[api['cluster']] == 1:
                api['cluster_type'] = 'atomic'

        return apis

    def _refine_crud_clusters(self, apis: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        """
        Refine clusters to ensure CRUD completeness.

        If a cluster has APIs with the same entity but different CRUD operations,
        keep them together. Otherwise, split them.
        """
        # Group by cluster
        clusters = defaultdict(list)
        for api in apis:
            clusters[api['cluster']].append(api)

        refined_apis = []
        next_cluster_id = max(clusters.keys()) + 1 if clusters else 0

        for cluster_id, cluster_apis in clusters.items():
            if len(cluster_apis) == 1:
                refined_apis.extend(cluster_apis)
                continue

            # Check if all APIs share the same entity anchor
            entities = set(api['entity_anchor'] for api in cluster_apis)

            if len(entities) == 1:
                # Same entity - check CRUD operations
                crud_ops = set()
                for api in cluster_apis:
                    method = api['method']
                    crud_op = self.METHOD_TO_CRUD.get(method, 'other')
                    crud_ops.add(crud_op)

                # If multiple CRUD operations, this is a good cluster
                if len(crud_ops) > 1:
                    for api in cluster_apis:
                        api['cluster_type'] = 'crud_complete'
                    refined_apis.extend(cluster_apis)
                else:
                    # Single operation type - keep as is
                    refined_apis.extend(cluster_apis)
            else:
                # Multiple entities in same cluster - need to split
                entity_groups = defaultdict(list)
                for api in cluster_apis:
                    entity_groups[api['entity_anchor']].append(api)

                for entity, entity_apis in entity_groups.items():
                    if len(entity_apis) == 1:
                        entity_apis[0]['cluster'] = next_cluster_id
                        entity_apis[0]['cluster_type'] = 'atomic'
                        next_cluster_id += 1
                    else:
                        for api in entity_apis:
                            api['cluster'] = next_cluster_id
                            api['cluster_type'] = 'entity_grouped'
                        next_cluster_id += 1
                    refined_apis.extend(entity_apis)

        return refined_apis

    def calculate_schema_similarity(self, api1: Dict[str, Any],
                                    api2: Dict[str, Any]) -> float:
        """
        Calculate schema similarity between two APIs using Jaccard index.

        Weighted: Response schema (70%) + Request schema (30%)
        """
        # Response schema similarity
        response_fields1 = self._get_schema_fingerprint(api1, 'response')
        response_fields2 = self._get_schema_fingerprint(api2, 'response')

        if response_fields1 or response_fields2:
            response_sim = self._jaccard_similarity(response_fields1, response_fields2)
        else:
            response_sim = 0.0

        # Request schema similarity
        request_fields1 = self._get_schema_fingerprint(api1, 'request')
        request_fields2 = self._get_schema_fingerprint(api2, 'request')

        if request_fields1 or request_fields2:
            request_sim = self._jaccard_similarity(request_fields1, request_fields2)
        else:
            request_sim = 0.0

        # Weighted combination
        total_sim = (response_sim * self.response_weight +
                    request_sim * self.request_weight)

        return total_sim

    def _jaccard_similarity(self, set1: Set[str], set2: Set[str]) -> float:
        """Calculate Jaccard similarity between two sets."""
        if not set1 and not set2:
            return 1.0
        if not set1 or not set2:
            return 0.0

        intersection = len(set1 & set2)
        union = len(set1 | set2)

        return intersection / union if union > 0 else 0.0
